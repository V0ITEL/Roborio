CREATE OR REPLACE FUNCTION "public"."debug_request_headers"() RETURNS "jsonb"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
DECLARE
    headers_raw text;
    headers_json jsonb;
BEGIN
    -- Get raw headers from request context
    headers_raw := current_setting('request.headers', true);

    IF headers_raw IS NULL OR headers_raw = '' THEN
        RETURN jsonb_build_object(
            'error', 'No headers found',
            'x_wallet_present', false,
            'authorization_present', false
        );
    END IF;

    -- Parse headers as JSON
    BEGIN
        headers_json := headers_raw::jsonb;
    EXCEPTION WHEN OTHERS THEN
        RETURN jsonb_build_object(
            'error', 'Failed to parse headers as JSON',
            'raw_headers', headers_raw
        );
    END;

    RETURN jsonb_build_object(
        'x_wallet_present', headers_json ? 'x-wallet',
        'x_wallet_value', headers_json->>'x-wallet',
        'authorization_present', headers_json ? 'authorization',
        'all_header_keys', (SELECT jsonb_agg(key) FROM jsonb_object_keys(headers_json) AS key)
    );
END;
$$;


